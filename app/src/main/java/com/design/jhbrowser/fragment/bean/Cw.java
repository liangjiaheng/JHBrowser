public class Cw {
    private String w;
    private int sc;

    public String getW() {
        return w;
    }

    public void setW(String w) {
        this.w = w;
    }

    public int getSc() {
        return sc;
    }

    public void setSc(int sc) {
        this.sc = sc;
    }

    @Override
    public String toString() {
        return "Cw{" +
                "w='" + w + '\'' +
                ", sc=" + sc +
                '}';
    }
}
